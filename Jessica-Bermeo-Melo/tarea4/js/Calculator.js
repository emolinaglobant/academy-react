class Calculator{

    division(num1, num2){
        if(num2 === 0) return;
        return num1 / num2;
    }

    multiplication (num1, num2){
        return num1 * num2;
    }

    subtraction(num1, num2){
        return num1 - num2;
    }

    sum(num1, num2){
        return num1 + num2;
    }
    
}